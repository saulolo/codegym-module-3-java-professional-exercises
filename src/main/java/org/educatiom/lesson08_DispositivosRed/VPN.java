package org.educatiom.lesson08_DispositivosRed;


public class VPN {

    public static void main(String[] args) {

        System.out.println("--------------------- VPN ---------------------------");
        //Introducción a VPN
        /*Virtual Private Network o VPN es literalmente una red privada virtual.
        Inicia un cliente VPN en su computadora y lo usa para conectarse al servidor VPN, por lo que la computadora
        ahora piensa que está dentro de la red local donde se encuentra el servidor VPN.
         */

        System.out.println("--------------------- Tipos de VPN ---------------------------");
        //VPN de intranet
        /*Se utiliza para combinar varias sucursales distribuidas de una organización en una sola red segura,
        intercambiando datos a través de canales de comunicación abiertos. Este es el primero que lo empezó todo.

        //VPN de acceso remoto
        Se utiliza para crear un canal seguro entre un segmento de la red corporativa (oficina central o sucursal) y un
        solo usuario que, mientras trabaja en casa, se conecta a los recursos corporativos desde una computadora
        doméstica, una computadora portátil corporativa, un teléfono inteligente o un quiosco de Internet. Esta es la
        opción que tienes si trabajas desde casa y te conectas a la oficina a través de una VPN.

        //VPN de extranet
        Se utiliza para redes a las que se conectan usuarios "externos" (por ejemplo, clientes o clientes). El nivel de
        confianza en ellos es muy inferior al de los empleados de la empresa, por lo que es necesario establecer “fronteras”
        especiales de protección que impidan o restrinjan el acceso de estos últimos a información confidencial de especial
        valor.

        //Internet VPN
        Utilizado por los proveedores para brindar acceso a Internet, generalmente si varios usuarios se conectan a
        través de un canal físico. El protocolo PPPoE se ha convertido en el estándar en las conexiones ADSL.

        //VPN cliente/servidor
        También una opción popular. Garantiza la protección de los datos transmitidos entre dos nodos (no redes) de una
        red corporativa. La particularidad de esta opción es que la VPN se construye entre nodos que suelen estar ubicados
        en el mismo segmento de red, por ejemplo, entre una estación de trabajo y un servidor.

        */

        System.out.println("--------------------- Open VPN ---------------------------");
        /*OpenVPN es un programa gratuito que implementa la tecnología de red privada virtual (VPN). Admite dos modos de
        operación populares: cliente-servidor y punto a punto, cuando necesita combinar dos redes grandes.

       */

    }
}
